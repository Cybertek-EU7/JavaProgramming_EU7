package week3;

public class BasicLoopExamples {

	public static void main(String[] args) {

		for(int i=0 ; i<10 ; i+=1 )	{
			System.out.print("Hello  ");
		}

	//	System.out.println(i); just local to the for loop
		System.out.println("");
		System.out.println("--------------------------------------");
		
		for(int i=1; i<=10 ; i++) {
			
			System.out.println("2 X "+i+" = "+(2*i));
		}
	}

}
