public class Task29_BiggerNumber {

    public static void main(String[] args) {

        int num1 = 500;
        int num2 = 200;

        if (num1 > num2) {
            System.out.println("Num1:" + num1 + " is bigger than " + "Num2:" + num2);
        } else {
            System.out.println("Num2:" + num2 + " is bigger than " + "Num1:" + num1);
        }
    }

}
